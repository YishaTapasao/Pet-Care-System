    private void oTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_oTableMouseClicked
     
        ofname.setEnabled(false);
        olname.setEnabled(false);
        int rowIndex = oTable.getSelectedRow();
      if(rowIndex < 0){
          JOptionPane.showMessageDialog(null, "Please select an item!");         
      }else{
          try{
              dbConnector dbc = new dbConnector();
              TableModel tbl = oTable.getModel();
              ResultSet rs = dbc.getData("SELECT * FROM tbl_owner WHERE owner_id ='"+tbl.getValueAt(rowIndex, 0)+"'");
              if(rs.next()){
                  createUserForm crf = new createUserForm();
                  oid.setText(""+rs.getInt("owner_id"));
                  ofname.setText(""+rs.getString("owner_fname"));
                  olname.setText(""+rs.getString("owner_lname"));
                  ocontact.setText(""+rs.getString("owner_contact"));
                  oaddress.setText(""+rs.getString("owner_address"));

                  add.setEnabled(false);
                  addlabel.setForeground(red);
                  checkadd = false;

                  

              }
          }catch(SQLException ex){
              System.out.println(""+ex);
          }
          
      } 
    }//GEN-LAST:event_oTableMouseClicked

