    public boolean duplicateOwnerCheck() {
    dbConnector dbc = new dbConnector();
    boolean isDuplicate = false;

    String query = "SELECT * FROM tbl_owner WHERE owner_fname = '" + ofname.getText() +
                   "' AND owner_lname = '" + olname.getText() +
                   "' AND owner_contact = '" + ocontact.getText() + "'";

    try {
        ResultSet rs = dbc.getData(query);
        if (rs.next()) {
            isDuplicate = true;
        }
    } catch (SQLException ex) {
        System.out.println("Duplicate check error: " + ex.getMessage());
    }

    return isDuplicate;
}
    
